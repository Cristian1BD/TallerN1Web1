let dia = 5;
let numeroDdia

switch(dia){
    case 1:
        numeroDdia = ("lunes")
        break;
    case 2:
        numeroDdia = ("martes")
        break;
    case 3:
        numeroDdia = ("miercoles")
        break;
    case 4:
        numeroDdia = ("jueves")
        break;
    case 5:
        numeroDdia = ("viernes")
        break;
    case 6:
        numeroDdia = ("sabado")
        break;
    case 7:
        numeroDdia = ("domingo")
        break;
}
console.log ("El numero del dia es " + numeroDdia )
